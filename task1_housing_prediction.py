feature = data[['Avg. Area Income', 'Avg. Area House Age', 'Avg. Area Number of Rooms',
       'Avg. Area Number of Bedrooms', 'Area Population']]
label = data["Price"]

from sklearn.model_selection import train_test_split
from sklearn.linear_model import LinearRegression

x_train,x_test,y_train,y_test = train_test_split(feature,label,test_size=0.2,random_state=42)#42 is standard vlaue for testing

lm = LinearRegression()
lm.fit(x_train,y_train)

pred= lm.predict(x_test)

from sklearn.metrics import classification_report,confusion_matrix

std_preds = pd.DataFrame({'Actual':y_test.squeeze(),'Predicted':pred.squeeze()})#sueeze-changing dimensions
